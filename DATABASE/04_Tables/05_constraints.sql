/*
CONSTRAINTS
*/

NOT NULL- Asegura que una columna no pueda tener un valor NULL
UNIQUE- Asegura que todos los valores en una columna son diferente
PRIMARY KEY - Una combinación de un NOT NULL y UNIQUE. Identifica de manera única cada fila de una tabla
FOREIGN KEY - Previene acciones que destruirían los vínculos entre tablas
CHECK- Asegura eso los valores en una columna satisfacen una condición específica
DEFAULT- Establece un valor predeterminado para una columna si no hay valor se especifica
CREATE INDEX- Se utiliza para crear y recuperar datos de la base de datos muy rápido
AUTO_INCREMENT- Autoincremente el valor generado automáticamente

