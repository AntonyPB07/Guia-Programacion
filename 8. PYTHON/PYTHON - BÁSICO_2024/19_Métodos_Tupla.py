# count() ---> Devuelve el número de veces que un elemento específico aparece en la tupla.
tupla = (1, 2, 2, 3, 2, 4)
cantidad = tupla.count(2)
print(cantidad)

# index() ---> Devuelve el índice de la primera aparición de un elemento específico en la tupla.
tupla = (10, 20, 30, 20, 40)
indice = tupla.index(20)
print(indice)