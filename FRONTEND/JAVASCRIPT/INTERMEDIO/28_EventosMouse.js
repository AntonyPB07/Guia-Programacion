/*
? Los eventos de mouse en JavaScript son aquellos eventos que se disparan cuando se interactúa con el ratón en una página web. 
?   Algunos de los eventos de mouse más comunes son:

! Eventos de clic:
click: Se produce cuando se hace clic en un elemento.
dblclick: Se produce cuando se hace doble clic en un elemento.
contextmenu: Se produce cuando se presiona el botón derecho del mouse en un elemento.

! Eventos de movimiento:
mousemove: Se produce cuando se mueve el mouse sobre un elemento.
mouseover: Se produce cuando el mouse ingresa a un elemento.
mouseout: Se produce cuando el mouse sale de un elemento.
mouseenter: Se dispara cuando el ratón entra en un elemento, pero no se propaga a sus hijos.
mouseleave: Se dispara cuando el ratón sale de un elemento, pero no se propaga a sus hijos.

! Eventos de presión:
mousedown: Se produce cuando se presiona un botón del mouse sobre un elemento.
mouseup: Se produce cuando se suelta un botón del mouse sobre un elemento.
*/