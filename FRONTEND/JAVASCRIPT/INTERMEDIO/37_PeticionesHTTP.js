/*
? Una solicitud HTTP, también conocida como httpRequest, es un mensaje que un cliente envía a un servidor web para solicitar un recurso. 
?   Este mensaje se compone de varios elementos, como:

Método de solicitud: Indica la acción que el cliente desea realizar sobre el recurso. Los métodos más comunes son GET, POST, PUT y DELETE.
URL: La dirección del recurso que se está solicitando.
Encabezados: Pares clave-valor que proporcionan información adicional sobre la solicitud, como el tipo de contenido que se espera recibir o 
    las cookies que se deben enviar.
Cuerpo: La información que se envía al servidor, como un formulario o un archivo.

* Las solicitudes HTTP se utilizan para:
Recuperar recursos: Obtener páginas web, imágenes, vídeos, etc.
Enviar datos: Enviar formularios, registrarse en un sitio web, etc.
Eliminar recursos: Eliminar archivos, mensajes, etc.
Modificar recursos: Editar información, actualizar un perfil, etc.

* ¿Cómo funciona una solicitud HTTP?
El proceso de una solicitud HTTP se puede dividir en los siguientes pasos:
El cliente envía una solicitud al servidor.
El servidor recibe la solicitud y la procesa.
El servidor envía una respuesta al cliente.
El cliente recibe la respuesta y la procesa.

* Aquí hay algunos ejemplos de solicitudes HTTP comunes:
GET /index.html: Solicitar la página web "index.html".
POST /login.php: Enviar un formulario de inicio de sesión.
PUT /api/users/123: Actualizar la información de un usuario con el ID 123.
DELETE /api/files/my-file.txt: Eliminar el archivo "my-file.txt".
*/