//! Atributos Estándares Básicos
/*
accesskey: Especifica una tecla de acceso rápido para acceder al elemento.
class: Permite asignar una o más clases al elemento para su identificación y estilo.
contenteditable: Indica si el contenido del elemento puede ser editado por el usuario.
contextmenu: Especifica el menú contextual que se mostrará al hacer clic derecho en el elemento.
data-* Permite almacenar datos personalizados en el elemento.
dir: Indica la dirección del texto en el elemento.
hidden: Oculta el elemento de la vista.
id: Asigna un identificador único al elemento.
lang: Indica el idioma del contenido del elemento.
style: Permite aplicar estilos CSS directamente al elemento.
tabindex: Indica el orden en que se puede acceder al elemento mediante la tecla Tab.
title: Muestra una información emergente al pasar el cursor sobre el elemento.
*/

