setAttribute(); //Este método se utiliza para establecer el valor de un atributo en un elemento HTML.
elemento.setAttribute("atributo", "valor");

getAttribute(); //Este método se utiliza para obtener el valor de un atributo de un elemento HTML.
let valor = elemento.getAttribute("atributo");

hasAttribute(); //Este método se utiliza para comprobar si un elemento tiene un atributo específico.
let tieneAtributo = elemento.hasAttribute("atributo");

removeAttribute(); //Este método se utiliza para eliminar un atributo de un elemento HTML.
elemento.removeAttribute("atributo");