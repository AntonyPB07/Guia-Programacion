//! Propiedades:
Math.E// Constante de Euler, la base del logaritmo natural, aproximadamente 2.718.
Math.PI// La relación entre la circunferencia de un círculo y su diámetro, aproximadamente 3.14159.
Math.LN2// El logaritmo natural de 2, aproximadamente 0.693.
Math.LN10// El logaritmo natural de 10, aproximadamente 2.303.
Math.LOG2E// El logaritmo en base 2 de E, aproximadamente 1.442.
Math.LOG10E// El logaritmo en base 10 de E, aproximadamente 0.434.
Math.SQRT1_2// La raíz cuadrada de 1/2, aproximadamente 0.707.
Math.SQRT2// La raíz cuadrada de 2, aproximadamente 1.414.

//! Métodos:
Math.abs(x)// Devuelve el valor absoluto de un número.
Math.acos(x)// Devuelve el arcocoseno de un número.
Math.acosh(x)// Devuelve el arcocoseno hiperbólico de un número.
Math.asin(x)// Devuelve el arcoseno de un número.
Math.asinh(x)// Devuelve el arcoseno hiperbólico de un número.
Math.atan(x)// Devuelve la arcotangente de un número.
Math.atanh(x)// Devuelve la arcotangente hiperbólica de un número.
Math.atan2(y, x)// Devuelve la arcotangente del cociente de sus argumentos.
Math.cbrt(x)// Devuelve la raíz cúbica de un número.
Math.ceil(x)// Devuelve el entero más pequeño mayor o igual que un número.
Math.clz32(x)// Devuelve el número de ceros iniciales en la representación binaria de 32 bits de un número.
Math.cos(x)// Devuelve el coseno de un número.
Math.cosh(x)// Devuelve el coseno hiperbólico de un número.
Math.exp(x)// Devuelve E elevado a la potencia de un número.
Math.expm1(x)// Devuelve E elevado a la potencia de un número, menos 1.
Math.floor(x)// Devuelve el entero más grande menor o igual que un número.
Math.fround(x)// Devuelve el número más cercano representable en precisión de 32 bits.
Math.hypot(...args)// Devuelve la raíz cuadrada de la suma de los cuadrados de sus argumentos.
Math.imul(x, y)// Devuelve el resultado de una multiplicación de enteros de 32 bits.
Math.log(x)// Devuelve el logaritmo natural de un número.
Math.log1p(x)// Devuelve el logaritmo natural de 1 más el número.
Math.log10(x)// Devuelve el logaritmo en base 10 de un número.
Math.log2(x)// Devuelve el logaritmo en base 2 de un número.
Math.max(...args)// Devuelve el número más grande de una lista de argumentos.
Math.min(...args)// Devuelve el número más pequeño de una lista de argumentos.
Math.pow(x, y)// Devuelve la base elevada a la potencia de un exponente.
Math.random()// Devuelve un número pseudo-aleatorio entre 0 (inclusive) y 1 (exclusivo).
Math.round(x)// Devuelve el valor redondeado al número entero más cercano.
Math.sign(x)// Devuelve el signo de un número, indicando si es positivo, negativo o cero.
Math.sin(x)// Devuelve el seno de un número.
Math.sinh(x)// Devuelve el seno hiperbólico de un número.
Math.sqrt(x)// Devuelve la raíz cuadrada positiva de un número.
Math.tan(x)// Devuelve la tangente de un número.
Math.tanh(x)// Devuelve la tangente hiperbólica de un número.
Math.trunc(x)// Devuelve la parte entera de un número eliminando los dígitos fraccionarios.