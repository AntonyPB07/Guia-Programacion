//! Método de registro:
console.log()// Imprime un mensaje en la consola.
console.info()// Imprime un mensaje informativo en la consola.
console.warn()// Imprime un mensaje de advertencia en la consola.
console.error()// Imprime un mensaje de error en la consola.

//! Metodos de conteo:
console.count()// Registra el número de veces que se llama a.
count()// Esta función toma como argumento opcional una etiqueta.
console.countReset()// Restablece el contador de console.count()

//! Métodos de Agrupación:
console.group()// Inicia un nuevo grupo de mensajes en la consola.
console.groupCollapsed()// Inicia un nuevo grupo de mensajes colapsado en la consola.
console.groupEnd()// Finaliza el grupo actual en la consola.

//! Métodos de Temporización:
console.time()// Inicia un temporizador que puede utilizarse para medir el tiempo de ejecución de una operación.
console.timeEnd()// Detiene el temporizador previamente iniciado y registra el tiempo transcurrido.