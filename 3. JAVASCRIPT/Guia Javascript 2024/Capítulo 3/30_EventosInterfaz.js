Eventos_de_formulario:
submit //Se produce cuando se envía un formulario.
change //Se produce cuando se modifica el valor de un elemento del formulario (como un campo de texto).
input //Se produce cuando se ingresa un valor en un elemento del formulario (como un campo de texto).

Eventos_de_selección:
focus //Se produce cuando un elemento recibe el foco (se selecciona).
blur //Se produce cuando un elemento pierde el foco.